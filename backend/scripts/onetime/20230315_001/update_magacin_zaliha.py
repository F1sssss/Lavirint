from backend import models as m
from backend.db import db


def update_magacin_zaliha_by_id(magacin_zaliha_id: int):
    magacin_zaliha = db.session.query(m.MagacinZaliha).get(magacin_zaliha_id)

    artikal = m.Artikal()
    artikal.sifra = magacin_zaliha.artikal.sifra
    artikal.barkod = magacin_zaliha.artikal.barkod
    artikal.naziv = magacin_zaliha.artikal.naziv
    artikal.opis = magacin_zaliha.artikal.opis
    artikal.jedinica_mjere = magacin_zaliha.artikal.jedinica_mjere
    artikal.jedinica_mjere_id = magacin_zaliha.artikal.jedinica_mjere_id
    artikal.grupa_artikala = magacin_zaliha.artikal.grupa_artikala
    artikal.grupa_artikala_id = magacin_zaliha.artikal.grupa_artikala_id
    db.session.add(artikal)

    magacin_zaliha.artikal = artikal
    db.session.add(magacin_zaliha)
    db.session.commit()

